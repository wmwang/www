<p> <STRONG>A、網上訂購</STRONG></p>
<p><BR>
  □□□□□□□   
  選擇您心儀的商品後，點擊「購買」將商品添加到您的購物車，在購物清單中可以對商品進行刪除或修改其訂購數量等。然後進入結算中心，請準確填寫收貨人信息並選擇付款方式，確認信息，提交訂單，客服人員在看到您的定單後會立即與您聯繫確認定單。</p>
